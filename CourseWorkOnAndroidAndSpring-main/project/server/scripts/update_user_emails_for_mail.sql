-- =============================================================================
-- Скрипт для PgAdmin: чтобы письма приходили на вашу почту
-- =============================================================================
-- Письма отправляются на тот email, который записан в таблице users для
-- текущего пользователя. Сейчас там тестовые адреса (admin@mi.vlgu.ru и т.д.),
-- на них ничего не доставляется.
--
-- Что сделать:
-- 1. Ниже замените YOUR_REAL_EMAIL@mail.ru на вашу настоящую почту (Mail.ru,
--    Yandex, Gmail — ту, для которой настроили пароль приложения в application.yml).
-- 2. Выполните этот скрипт в PgAdmin (Query Tool) к той же БД, к которой
--    подключается приложение.
-- 3. В application.yml задайте spring.mail.host, username, password и
--    перезапустите приложение.
-- 4. Входите под admin — письмо при входе/отчёте придёт на указанный email.
-- =============================================================================

-- Поставьте свой реальный email для учётки admin (на неё будут приходить письма при входе и отчётах):
UPDATE users SET email = 'YOUR_REAL_EMAIL@mail.ru' WHERE login = 'admin';

